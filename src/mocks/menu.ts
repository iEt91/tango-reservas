import type { MenuCategory, MenuItem } from "@/data/types";

const timestamp = "2026-06-11T10:00:00.000Z";

function createCategory(
  businessId: string,
  suffix: string,
  name: string,
  sortOrder: number,
  description?: string,
): MenuCategory {
  return {
    id: `menu-${businessId}-${suffix}`,
    businessId,
    name,
    description: description ?? null,
    sortOrder,
    isActive: true,
    createdAt: timestamp,
    updatedAt: timestamp,
  };
}

function createItem(
  businessId: string,
  suffix: string,
  categoryId: string,
  name: string,
  sortOrder: number,
  description: string,
  price: number | null,
  imagePlaceholder: string,
  tags: string[] = [],
  isFeatured = false,
): MenuItem {
  return {
    id: `menu-${businessId}-${suffix}`,
    businessId,
    categoryId,
    name,
    description,
    price,
    imageUrl: null,
    imagePlaceholder,
    isActive: true,
    isFeatured,
    sortOrder,
    tags,
    createdAt: timestamp,
    updatedAt: timestamp,
  };
}

